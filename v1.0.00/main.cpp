void character_callback(GLFWwindow* window, unsigned int codepoint) {
    if (!consoleOpen) return;
    if (codepoint >= 32 && codepoint <= 126) {
        consoleInput += static_cast<char>(codepoint);
    }
}

void key_callback(GLFWwindow* window, int key, int scancode, int action, int mods) {
    // Console toggle (works in all states)
    if (key == GLFW_KEY_0 && action == GLFW_PRESS) {
        consoleOpen = !consoleOpen;
        consoleInput.clear();
        return;
    }

    if (consoleOpen) {
        if (action == GLFW_PRESS) {
            if (key == GLFW_KEY_ENTER) {
                if (!consoleInput.empty()) {
                    consoleHistory.push_back(consoleInput);
                    historyIndex = consoleHistory.size();
                    executeCommand(consoleInput);
                    consoleInput.clear();
                }
            } else if (key == GLFW_KEY_BACKSPACE) {
                if (!consoleInput.empty()) consoleInput.pop_back();
            } else if (key == GLFW_KEY_UP) {
                if (historyIndex > 0) {
                    historyIndex--;
                    consoleInput = consoleHistory[historyIndex];
                }
            } else if (key == GLFW_KEY_DOWN) {
                if (historyIndex < consoleHistory.size()) {
                    historyIndex++;
                    if (historyIndex < consoleHistory.size())
                        consoleInput = consoleHistory[historyIndex];
                    else
                        consoleInput.clear();
                }
            } else if (key == GLFW_KEY_ESCAPE) {
                consoleOpen = false;
            }
        }
        return;
    }

    // Normal game / menu input (console closed)
    if (action == GLFW_PRESS) {
        if (key >= 0 && key < 1024) keys[key] = true;
        if (key == GLFW_KEY_ESCAPE && appState == AppState::GAME) {
            cleanupGame(window);
            appState = AppState::MENU;
            keys[key] = false; // prevent immediate re‑trigger
        }
    } else if (action == GLFW_RELEASE) {
        if (key >= 0 && key < 1024) keys[key] = false;
    }
}

void mouse_button_callback(GLFWwindow* window, int button, int action, int mods) {
    //std::cout << "Mouse button " << button << " action " << action << std::endl;
    if (button >= 0 && button < 8) {
        if (action == GLFW_PRESS) {
            mouseButtons[button] = true;
            mouseJustPressed[button] = true;
        } else if (action == GLFW_RELEASE) {
            mouseButtons[button] = false;
        }
    }
}

void cursor_pos_callback(GLFWwindow* window, double xpos, double ypos) {
    mouseX = xpos;
    mouseY = ypos;

    // Only update camera in game state (console closed)
    if (appState == AppState::GAME && !consoleOpen && g_camera) {
        if (firstMouse) {
            lastX = xpos;
            lastY = ypos;
            firstMouse = false;
        }
        float xoffset = xpos - lastX;
        float yoffset = lastY - ypos;
        lastX = xpos;
        lastY = ypos;
        g_camera->processMouseMovement(xoffset, yoffset);
    }
}

