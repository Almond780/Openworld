bool startGame(GLFWwindow* window) {
    //std::cout << "startGame called" << std::endl;
    // 1. Create player (owned by g_playerPtr)
    g_playerPtr = std::make_unique<Player>(glm::vec3(0.0f, 2.0f, 5.0f));
    g_player = g_playerPtr.get();

    // 2. Physics world (global object, reset first)
    g_physics = PhysicsWorld();
    g_physics.setGravity(glm::vec3(0.0f, -8.0f, 0.0f));
    g_physics.addRigidBody(g_player->getRigidBody());
    g_physicsWorld = &g_physics;

    // 3. Camera (owned by g_cameraPtr)
    g_cameraPtr = std::make_unique<FirstPersonCamera>(g_player, glm::vec3(0.0f, 1.0f, 0.0f));
    g_camera = g_cameraPtr.get();
    glfwSetWindowUserPointer(window, g_camera);

    // 4. Ground plane
    g_groundPlane = std::make_unique<Plane>(100.0f, 100.0f);
    g_physics.addCollisionPlane(glm::vec3(0.0f, 1.0f, 0.0f), 0.0f);

    // 5. Level definition
    // Tutorial
    Level tutorial;
    tutorial.name = "Tutorial";
    tutorial.initialSpawn = glm::vec3(0.0f, 2.0f, 5.0f);
    tutorial.checkpoints = {
        { glm::vec3(50.0f, 2.0f, 50.0f), "CP1" },
        { glm::vec3(-50.0f, 2.0f, -50.0f), "CP2" },
        { glm::vec3(-50.0f, 2.0f, 50.0f), "CP3" },
        { glm::vec3(50.0f, 2.0f, -50.0f), "CP4" }
    };
    g_levelManager.registerLevel(tutorial);
    g_levelManager.loadLevel("Tutorial");
    g_player->getRigidBody()->position = g_levelManager.getRespawnPosition();

    // Level 1
    Level level1;
    level1.name = "Level1";
    level1.initialSpawn = glm::vec3(100.0f, 2.0f, 100.0f);
    level1.checkpoints = {
        { glm::vec3(50.0f, 2.0f, 70.0f), "L1_CP1" },
        { glm::vec3(80.0f, 2.0f, 80.0f), "L1_CP2" },
        { glm::vec3(100.0f, 5.0f, 100.0f), "L1_CP3" },
        { glm::vec3(120.0f, 2.0f, 120.0f), "L1_CP4" }
    };
    g_levelManager.registerLevel(level1);

    // 6. Objects
    g_worldObjects.clear();
    g_dynamicObjects.clear();
    g_worldObjects.reserve(1000);
    buildWorld(g_worldObjects, g_physics, g_dynamicObjects, *g_player);

    // 7. Hide mouse cursor, reset first-mouse
    glfwSetInputMode(window, GLFW_CURSOR, GLFW_CURSOR_DISABLED);
    firstMouse = true;
    //std::cout << "startGame called" << std::endl;
    return true;
}

void cleanupGame(GLFWwindow* window) {
    // 1. Free dynamic RigidBodies (we own them via new in ObjectFactory)
    for (auto& pair : g_dynamicObjects) {
        delete pair.second;
    }
    g_dynamicObjects.clear();

    // 2. Clear objects (meshes freed automatically)
    g_worldObjects.clear();

    // 3. Reset unique_ptrs (destroys Player, Camera, Ground)
    g_playerPtr.reset();
    g_cameraPtr.reset();
    g_groundPlane.reset();

    // 4. Reset globals
    g_player = nullptr;
    g_physicsWorld = nullptr;
    g_camera = nullptr;

    // 5. Show cursor again
    glfwSetInputMode(window, GLFW_CURSOR, GLFW_CURSOR_NORMAL);
}

